  CONSTRAINT PKCM PRIMARY KEY (N_PLATO, ID_FACTURA, ID_RESTAURANTE),
  CONSTRAINT FKCM1 FOREIGN KEY (ID_RESTAURANTE, N_PLATO) REFERENCES platos(ID_RESTAURANTE, N_PLATO) ON DELETE RESTRICT,
  CONSTRAINT FKCM2 FOREIGN KEY (ID_FACTURA, ID_RESTAURANTE) REFERENCES facturas(ID_FACTURA, ID_RESTAURANTE) ON DELETE RESTRICT,
  CONSTRAINT FKCM3 FOREIGN KEY (ID_RESTAURANTE) REFERENCES restaurantes(ID_RESTAURANTE) ON DELETE RESTRICT
